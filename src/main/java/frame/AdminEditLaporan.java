    private void tampilkanDataLaporan() {
        try {
            if (laporan != null) {
                txtIdLaporan.setText(laporan.getId_laporan());
                txtIdUser.setText(laporan.getUserPelapor() != null
                        ? laporan.getUserPelapor().getId_user()
                        : "-");
                txtJudul.setText(laporan.getJudul_laporan());
                txtIsi.setText(laporan.getIsi_laporan());
                txtKategoriLaporan.setText(laporan.getKategori() != null ? laporan.getKategori() : "-");
                txtBukti.setText(laporan.getBukti() != null ? laporan.getBukti() : "-");
                txtTanggal.setText(laporan.getTanggal_laporan() != null ? laporan.getTanggal_laporan().toString() : "-");
                txtTingkatKeparahan.setText(
                        laporan.getTingkat_keparahan() != null
                                ? laporan.getTingkat_keparahan().toString()
                                : "0"
                );

                txtIdLaporan.setEditable(false);
                txtIdUser.setEditable(false);
                txtJudul.setEditable(false);
                txtIsi.setEditable(false);
                txtKategoriLaporan.setEditable(false);
                txtBukti.setEditable(false);
                txtTanggal.setEditable(false);

                txtTingkatKeparahan.setEditable(true);
            } else {
                JOptionPane.showMessageDialog(this,
                        "Data laporan tidak ditemukan!",
                        "Peringatan", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            logger.log(java.util.logging.Level.SEVERE, "Gagal menampilkan data laporan", e);
            JOptionPane.showMessageDialog(this,
                    "Terjadi kesalahan saat menampilkan data laporan.",
                    "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }

