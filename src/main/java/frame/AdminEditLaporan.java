    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String tingkatStr = txtTingkatKeparahan.getText().trim();

        if (tingkatStr.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Tingkat Keparahan tidak boleh kosong!",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            double tingkat = Double.parseDouble(tingkatStr);

            if (tingkat < 0.0 || tingkat > 5.0) {
                JOptionPane.showMessageDialog(this,
                        "Nilai Tingkat Keparahan harus berada antara 0.0 dan 5.0!",
                        "Input Tidak Valid", JOptionPane.WARNING_MESSAGE);
                return;
            }

            try (Session session = HibernateUtil.getSessionFactory().openSession()) {
                Transaction tx = session.beginTransaction();

                Laporan update = session.get(Laporan.class, txtIdLaporan.getText());
                if (update != null) {
                    update.setTingkat_keparahan(tingkat);
                    session.update(update);
                    tx.commit();

                    JOptionPane.showMessageDialog(this,
                            "Perubahan berhasil disimpan!",
                            "Sukses", JOptionPane.INFORMATION_MESSAGE);

                    logger.info("Admin " + (adminLogin != null ? adminLogin.getId_user() : "unknown")
                            + " mengubah tingkat keparahan laporan ID: " + update.getId_laporan());
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Laporan tidak ditemukan!",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                logger.log(java.util.logging.Level.SEVERE, "Gagal menyimpan perubahan laporan", e);
                JOptionPane.showMessageDialog(this,
                        "Terjadi kesalahan saat menyimpan data: " + e.getMessage(),
                        "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "Masukkan angka valid untuk tingkat keparahan!",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }   
    }//GEN-LAST:event_btnSimpanActionPerformed

